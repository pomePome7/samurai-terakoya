// 朝のあいさつを出力する関数を作成する
const sayGoodMorning = () => {
    console.log('おはようございます！');
    console.log('昨日はよく眠れましたか？');
    console.log('今日も一日頑張りましょう！');
}

// 夜のあいさつを出力する関数を定義する
const sayGoodEvening = () => {
    console.log('こんばんは！');
    console.log('今日も一日お疲れさまでした。');
}

// 朝のあいさつを出力する関数を呼び出す（1回目）
sayGoodMorning();

// 夜のあいさつを出力する関数を呼び出す（2回目）
sayGoodEvening();

// 関数の作り方
const 定数名 = () => {
    '一連の処理'
}
